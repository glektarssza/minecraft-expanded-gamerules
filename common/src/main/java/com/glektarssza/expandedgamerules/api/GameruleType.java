/**
 * An enumeration of possible types of gamerules.
 */
public enum GameruleType {
    /**
     * A boolean gamerule.
     */
    BOOLEAN,

    /**
     * An integer gamerule.
     */
    INTEGER,

    /**
     * A floating point number gamerule.
     */
    FLOAT,

    /**
     * A string gamerule.
     */
    STRING
}
